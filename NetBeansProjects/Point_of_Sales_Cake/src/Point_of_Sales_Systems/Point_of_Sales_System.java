        cost_of_Items.IceCream = cost_of_Items.Costgroup3 * Double.parseDouble(jtextfieldIceCream.getText());
        cost_of_Items.OreoCream = cost_of_Items.Costgroup3 * Double.parseDouble(jtextfieldOreoCream.getText());
       
        
        iSubtotal = cost_of_Items.GetAmount() + Double.parseDouble(jtextCakeType.getText());
        iTax = cost_of_Items.cFindTax(iSubtotal);
        iTotal = iSubtotal + iTax;
        
        
        String Subtotal = String.format("$%.2f", iSubtotal);
        jtextSubTotal.setText(Subtotal);
        
        String Tax = String.format("$%.2f", iTax);
        jtextTax.setText(Tax);
        
        String Total = String.format("$%.2f", iTotal);
        jtextTotal.setText(Total);
        
